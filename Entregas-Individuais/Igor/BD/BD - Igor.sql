select fu.nome as 'Funcionário', fu.email as 'E-mail', fu.telefone as 'Telefone', fi.nome as 'Empresa de atuação', concat(en.logradouro, ', ', en.numero, ' - ', ifnull(en.complemento, 'Sem complemento'), ' - ', en.cidade, '/', en.uf, ' - ', en.cep) as 'Endereço', ifnull(em.nomefantasia, em.razaosocial) as 'Empresa' from empresa as em join filial as fi on fi.empresa_idempresa = em.idEmpresa join endereco as en on en.filial_idfilial = fi.idFilial join funcionario as fu on fu.filial_idfilial = fi.idFilial;
